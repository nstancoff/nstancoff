public class Unit_Conversion_v1_Reference {
    public double ft_to_mm(double x) {
        return x * 304.8;
    }
    public double ft_to_cm(double x) {
        return x * 30.48;
    }
    public double ft_to_m(double x) {
        return x * .3048;
    }
    public double ft_to_in(double x) {
        return x * 12;
    }
    public double in_to_mm(double x) {
        return x * 25.4;
    }
    public double in_to_cm(double x) {
        return x * 2.54;
    }
    public double in_to_m(double x) {
        return x * .0254;
    }
    public double in_to_ft(double x) {
        return x / 12;
    }
    public double m_to_mm(double x) {
        return x * 1000;
    }
    public double m_to_cm(double x) {
        return x * 100;
    }
    public double m_to_in(double x) {
        return x * 100 / 2.54;
    }
    public double m_to_ft(double x) {
        return x * 100 / 30.48;
    }
    public double cm_to_mm(double x) {
        return x * 10;
    }
    public double cm_to_m(double x) {
        return x / 100;
    }
    public double cm_to_in(double x) {
        return x / 2.54;
    }
    public double cm_to_ft(double x) {
        return x / 30.48;
    }
    public double mm_to_cm(double x) {
        return x / 10;
    }
    public double mm_to_m(double x) {
        return x / 1000;
    }
    public double mm_to_in(double x) {
        return x / 25.4;
    }
    public double mm_to_ft(double x) {
        return x / 304.8;
    }
}
